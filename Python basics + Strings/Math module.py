#MATH FUNCTIONS IN MATH MODULE :- many useful mathematical functions
import math #import the math module's library functions

print(math.sqrt(10)) #prints the square root of 10
print(math.factorial(4)) #prints the factorial of 4
print(math.fabs(-10.23)) #absolute value of float 10.23 
print(math.log(10)) #natural log of x(log to the base e)
print(math.log10(10)) #base 10 logarithm of x
print(math.exp(1)) #e raised to x. In log e has an fixed value
print(math.trunc(5.7)) #truncates to integer
print(math.ceil(5.7)) #smallest integer >= x
print(math.floor(5.7)) #largest integer <= x
print(math.modf(5)) #factorial and integer parts of 