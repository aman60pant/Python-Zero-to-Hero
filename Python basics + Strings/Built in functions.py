
x=-167
print(abs(x)) #prints the absolute value of x
x=2;y=3
print(pow(x,y)) #prints value x^y 
print(x**y) #prints the same value as pow(x,y)
print(min(x,y)) #prints the minimum value
print(max(x,y)) #prints the maximum value
print(divmod(y,x)) #prints the pair (x//y,x % y)
print(bin(x)) #prints the binary equivalent of x
print(oct(x)) #prints the octal equivalent of x
print(hex(x)) #prints the hexadecimal equivalent of x
